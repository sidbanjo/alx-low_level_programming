/**
 * op_add - adds two ints
 * @a: first int
 * @b: second int
 *
 * Return: sum of int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two ints
 * @a: first int
 * @b: second int
 *
 * Return: subtraction of int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two ints
 * @a: first int
 * @b: second int
 *
 * Return: multiplication of int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two ints
 * @a: first int
 * @b: second int
 *
 * Return: division of int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of two ints
 * @a: first int
 * @b: second int
 *
 * Return: modulus of int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
